mocha.setup("bdd");
const { assert } = chai;

describe("HCF of a number or array", () => {
  it("gcd() works", () => {
    assert.equal(gcd(8, 36), 4);
    assert.equal(gcd(...[12, 8, 32]), 4);
  });
});

