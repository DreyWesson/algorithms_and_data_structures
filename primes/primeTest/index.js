mocha.setup("bdd");
const { assert } = chai;

describe("isPrime()", () => {
  it("checks if a num is prime number or not", () => {
    assert.equal(isPrime(7), true);
    assert.equal(isPrime(4), false);
    assert.equal(isPrime(97), true);
  });
});

mocha.run();
