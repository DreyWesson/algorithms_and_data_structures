function isPrime(num) {
  //Time complexity O(sqrt(N)): if sqrt(N) is !primeNum then (N !== primeNum )
  //so the loop only has to test until the sqrt(N)
  let sqrtnum = Math.sqrt(num) | 0;
  let prime = num != 1;

  for (let i = 2; i < sqrtnum + 1; i++) {
    num % i == 0 && (prime = false);
  }
  return prime;
}

