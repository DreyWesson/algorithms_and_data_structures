// MODULAR EXPONENTIATION
const powerMod = (base, exponent, modulus) => {
  // Time complexity O(N)
  modulus === 1 && 0;

  let value = 1;
  for (let i = 0; i < exponent; i++) {
    value = (value * base) % modulus;
  }
  return value;
};
console.log(powerMod(4, 3, 5));

