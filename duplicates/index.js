mocha.setup("bdd");
const { assert } = chai;

describe("removeDuplicates()", () => {
  it("removes duplicates in a string.", () => {
    assert.deepEqual(removeDuplicates("daree"), ["d", "a", "r", "e"]);
  });
  it("removes duplicates in an arr.", () => {
    assert.deepEqual(removeDuplicates(["Hello", "world", "world"]), [
      "Hello",
      "world",
    ]);
  });
});

mocha.run();
